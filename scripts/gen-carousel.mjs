// Build-time scan of /public/videos → a static TS manifest the carousel imports.
// Runs in Node before every build so the list is baked in; the Cloudflare
// Worker has no filesystem, so we must NOT read the dir at request time.
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const dir = path.join(root, "public", "videos");
let files = [];
try {
  const all = fs
    .readdirSync(dir)
    .filter((f) => /\.(mp4|webm|mov|m4v)$/i.test(f) && !f.startsWith("."));
  const carousel = all.filter((f) => /carousel/i.test(f));
  files = (carousel.length ? carousel : all.filter((f) => !/original|transcoded|source/i.test(f))).sort(
    (a, b) => {
      const na = parseInt((a.match(/\d+/) || ["0"])[0], 10);
      const nb = parseInt((b.match(/\d+/) || ["0"])[0], 10);
      return na - nb || a.localeCompare(b);
    },
  );
} catch {
  files = [];
}

const out =
  `// AUTO-GENERATED by scripts/gen-carousel.mjs — do not edit by hand.\n` +
  `// Regenerated before every build from the files in /public/videos.\n` +
  `export const CAROUSEL_VIDEOS: string[] = ${JSON.stringify(files, null, 2)};\n`;

fs.writeFileSync(path.join(root, "components", "marketing", "carousel-manifest.ts"), out);
console.log(`[gen-carousel] wrote ${files.length} clip(s) to carousel-manifest.ts`);
